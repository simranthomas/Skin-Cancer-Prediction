    public void readFrom() {
        try {
            ObjectInputStream in = new ObjectInputStream(new FileInputStream(new File(System.getProperty("user.dir") + "\\db.dat")));
            imageSet = (ImageSet) in.readObject();
            in.close();
        } catch (Exception e) {
            System.out.println("Error: " + e);
            writeTo();
        }


    }

    public void writeTo() {
        try {
            ObjectOutputStream out = new ObjectOutputStream(new FileOutputStream(new File(System.getProperty("user.dir") + "\\db.dat")));
            out.writeObject(imageSet);
            out.close();
        } catch (Exception e) {
            System.out.println("Error: " + e);


        }
    }

